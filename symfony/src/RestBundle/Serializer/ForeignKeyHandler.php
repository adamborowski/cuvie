namespace RestBundle\Serializer;


use JMS\Serializer\Context;
use JMS\Serializer\GraphNavigator;
use JMS\Serializer\Handler\SubscribingHandlerInterface;
use JMS\Serializer\JsonSerializationVisitor;

class ForeignKeyHandler implements SubscribingHandlerInterface
{
    public static function getSubscribingMethods()
    {
        return array(
            array(
                'direction' => GraphNavigator::DIRECTION_SERIALIZATION,
                'format' => 'json',
                'type' => 'RestBundle\Serializer\ForeignKeyType',
                'method' => 'serializeToJson',
            )
        );
    }

    public function serializeToJson(JsonSerializationVisitor $visitor, $entity, array $type, Context $context)
    {
        return $entity->getId();
    }
}